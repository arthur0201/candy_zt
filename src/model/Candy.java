package model;

public class Candy {
	private int id;
	private String candyName;
	private String candyTaste;
	private int pricae;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCandyName() {
		return candyName;
	}
	public void setCandyName(String candyName) {
		this.candyName = candyName;
	}
	public String getCandyTaste() {
		return candyTaste;
	}
	public void setCandyTaste(String candyTaste) {
		this.candyTaste = candyTaste;
	}
	public int getPricae() {
		return pricae;
	}
	public void setPricae(int pricae) {
		this.pricae = pricae;
	}

}
